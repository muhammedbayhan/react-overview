//Ternary Operators

//Ternary operators are used to assign a value to a variable based on some condition. It is the only JavaScript operator that takes three operands. The operator can have one or two values based on a condition.

//Syntax
const result = true;
result ? console.log("value1") : console.log("value2");
result == true ? console.log("value1") : console.log("value2");
